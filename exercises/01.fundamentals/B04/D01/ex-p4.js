// Faça um programa que, dado um valor definido numa variável, retorne 
// "positive" se esse valor for positivo, "negative" se for negativo e "zero" caso contrário.

let a = 0.3;

if (a > 0) {
    console.log("POSITIVO");
} else if (a < 0) {
    console.log("NEGATIVO");
} else {
    console.log("ZERO");
}
